# Пояснение:
# Animal - базовый класс для всех животных. Он имеет координаты, скорость, методы перемещения, получения координат, атаки и воспроизведения звука.
# Bird - наследует от Animal, добавляет атрибут beak и метод откладывания яиц lay_eggs.
# AquaticAnimal - наследует от Animal, изменяет степень опасности на 3 и добавляет метод ныряния dive_in.
# PoisonousAnimal - наследует от Animal, изменяет степень опасности на 8.
# Duckbill - класс, который наследует от Bird, AquaticAnimal и PoisonousAnimal. Задается звук, который утконос издает, и вызываются конструкторы родительских классов.
# Пример работы:
# Создание объекта Duckbill:
#
# При создании объекта утконоса duckbill, он наследует все атрибуты и методы от Bird, AquaticAnimal и PoisonousAnimal. Он также может выполнять действия, присущие каждому из этих классов.
# Движение:
#
# Метод move() позволяет двигаться по координатам с учетом скорости.
# Ныряние:
#
# Метод dive_in() изменяет координату Z в отрицательную сторону с учетом скорости.
# Звук и яйца:
#
# Метод speak() выводит звук утконоса, а lay_eggs() выводит случайное количество яиц.
# Атака:
#
# Метод attack() проверяет степень опасности и выводит соответствующее сообщение.

